import { Link, useSearchParams } from 'react-router-dom';
import {
  excerptFromJobHtml,
  loadDatasetManifest,
  loadJsonlDataset,
  type JsonlDatasetManifestEntry,
  type JsonlJobDescription,
} from '../data/jsonlJobs';

